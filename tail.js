// ---------------------------------------------------------------------------------------------------------
// tail function
// ---------------------------------------------------------------------------------------------------------

let tail = function(array) {
  return array.slice(1);
};

// ---------------------------------------------------------------------------------------------------------
// export
// ---------------------------------------------------------------------------------------------------------

module.exports = tail;

